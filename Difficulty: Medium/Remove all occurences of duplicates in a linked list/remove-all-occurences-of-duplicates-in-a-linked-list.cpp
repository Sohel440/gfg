class Solution {
  public:
    Node* removeAllDuplicates(struct Node* head) {
        // code here
        vector<int> a;
        Node* temp = head;
        unordered_map<int,int>mp;
        
        while(temp){
            
            
            a.push_back(temp->data);
            mp[temp->data]++;
            temp = temp->next;
        }
        
        Node * p = new Node(-1);
        Node *q = p;
        
        for(auto it : a){
            
            if(mp[it] == 1){
                 Node *t = new Node(it);
           p ->next = t;
           p = p ->next;
            }
          
           
            
        }
        
        return q->next;
    }
};

