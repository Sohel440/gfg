class Solution {
  public:
    int pairsum(vector<int> &arr) {
        // code here
        int mx =0;
        int smx=0;
        
        for(int i : arr){
            mx = max(mx , i);
        }
        
        for(int i : arr){
            if(mx >i){
                smx = max(smx , i);
            }
        }
        
        return smx+mx;
    }
};


